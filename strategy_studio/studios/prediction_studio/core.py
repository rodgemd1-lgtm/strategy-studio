# ── Forecast Record (v2) ───────────────────────────────────────────────────

class ForecastRecord(BaseModel):
    """Complete forecast record with all components."""
    forecast_id: str = Field(default_factory=lambda: hashlib.md5(str(datetime.now(timezone.utc).timestamp()).encode()).hexdigest()[:12])
    question: str
    resolution_criteria: str = ""
    deadline: datetime | None = None
    category: ForecastCategory = ForecastCategory.OPERATIONAL
    source_signals: list[str] = Field(default_factory=list)  # signal_ids

    # Priors
    base_rate: float | None = None
    market_priors: list[MarketPrior] = Field(default_factory=list)

    # Forecasts from different sources
    human_forecast: float | None = None
    llm_forecasts: list[LLMForecast] = Field(default_factory=list)
    model_forecasts: dict[str, float] = Field(default_factory=dict)

    # Causal analysis
    causal_tree_id: str | None = None

    # Ensemble and final
    ensemble_probability: float | None = None
    final_probability: float | None = None
    confidence: float = Field(default=0.5, ge=0.0, le=1.0)
    uncertainty_interval: tuple[float, float] | None = None

    # Evidence tracking
    evidence_for: list[str] = Field(default_factory=list)
    evidence_against: list[str] = Field(default_factory=list)
    evidence_updates: list[EvidenceUpdate] = Field(default_factory=list)

    # Missing information
    missing_information: list[str] = Field(default_factory=list)
    missing_info_tasks: list[MissingInfoTask] = Field(default_factory=list)

    # Watch indicators
    watch_indicators: list[str] = Field(default_factory=list)
    kill_conditions: list[str] = Field(default_factory=list)

    # Status and resolution
    status: ForecastStatus = ForecastStatus.DRAFT
    outcome: bool | None = None

    # Scoring
    scores: dict[str, float | None] = Field(default_factory=lambda: {
        "brier": None, "log_score": None, "interval_score": None, "calibration_bucket": None
    })

    # Postmortem
    postmortem: Postmortem | None = None

    # Metadata
    created_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))
    updated_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))
    notes: str = ""


# ── Forecast Store ─────────────────────────────────────────────────────────

class ForecastStore(BaseModel):
    """Store for all forecasts."""
    forecasts: dict[str, ForecastRecord] = Field(default_factory=dict)
    signal_registry: SignalRegistry = Field(default_factory=SignalRegistry)
    causal_trees: dict[str, CausalThesisTree] = Field(default_factory=dict)
    postmortems: dict[str, Postmortem] = Field(default_factory=dict)

    def add_forecast(self, forecast: ForecastRecord) -> None:
        self.forecasts[forecast.forecast_id] = forecast

    def get_forecast(self, forecast_id: str) -> ForecastRecord | None:
        return self.forecasts.get(forecast_id)

    def get_active(self) -> list[ForecastRecord]:
        return [f for f in self.forecasts.values() if f.status == ForecastStatus.ACTIVE]

    def get_by_category(self, category: ForecastCategory) -> list[ForecastRecord]:
        return [f for f in self.forecasts.values() if f.category == category]

    def get_resolved(self) -> list[ForecastRecord]:
        return [f for f in self.forecasts.values() if f.status == ForecastStatus.RESOLVED and f.outcome is not None]

    def get_calibration_data(self) -> list[tuple[float, bool]]:
        """Return (predicted_probability, outcome) pairs for resolved forecasts."""
        data = []
        for f in self.get_resolved():
            if f.final_probability is not None and f.outcome is not None:
                data.append((f.final_probability, f.outcome))
        return data

    def get_brier_score(self) -> float | None:
        """Compute overall Brier score for all resolved forecasts."""
        data = self.get_calibration_data()
        if not data:
            return None
        return sum((p - o) ** 2 for p, o in data) / len(data)

    def get_log_score(self) -> float | None:
        """Compute overall log score for all resolved forecasts."""
        data = self.get_calibration_data()
        if not data:
            return None
        score = 0.0
        for p, o in data:
            p = max(1e-10, min(1 - 1e-10, p))
            score += o * math.log(p) + (1 - o) * math.log(1 - p)
        return -score / len(data)