"""
Prediction Studio — Core schemas, forecast store, and signal registry.

Implements the closed-loop probabilistic decision system from the Consensus research review:
- Proper scoring rules (Brier, log, interval)
- Bayesian updating with likelihood ratios
- Prediction market priors
- Calibration tracking and reliability curves
- Missing-information engine with OmniScout integration
- Causal thesis trees with inversion tests
- Ensemble forecasting with multiple models

Based on 518 papers screened, 189 eligible, 50 included in structured review.
Key sources: Gneiting & Raftery (2007), Atanasov et al. (2017), Paleka et al. (2024).
"""
from __future__ import annotations

import hashlib
import json
import math
from datetime import datetime, timezone, timedelta
from enum import Enum
from pathlib import Path
from typing import Any, Literal

from pydantic import BaseModel, Field


# ── Enums ──────────────────────────────────────────────────────────────────

class ForecastCategory(str, Enum):
    PROJECT = "project"
    MARKET = "market"
    PERSONAL = "personal"
    TECH = "tech"
    STRATEGIC = "strategic"
    OPERATIONAL = "operational"


class ForecastStatus(str, Enum):
    DRAFT = "draft"
    ACTIVE = "active"
    RESOLVED = "resolved"
    ARCHIVED = "archived"


class ResolutionMatchQuality(str, Enum):
    EXACT = "exact"
    CLOSE = "close"
    WEAK = "weak"
    INVALID = "invalid"


# ── Signal Registry ────────────────────────────────────────────────────────

class Signal(BaseModel):
    """A single evidence signal ingested from any source."""
    signal_id: str = Field(default_factory=lambda: hashlib.md5(str(datetime.now(timezone.utc).timestamp()).encode()).hexdigest()[:12])
    source: str  # e.g., "omniscout", "heb", "odtb", "market", "web", "github"
    content: str
    url: str = ""
    timestamp: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))
    category: ForecastCategory = ForecastCategory.OPERATIONAL
    metadata: dict[str, Any] = Field(default_factory=dict)
    extracted_at: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))


class SignalRegistry(BaseModel):
    """Registry of all ingested signals."""
    signals: list[Signal] = Field(default_factory=list)

    def add_signal(self, signal: Signal) -> None:
        self.signals.append(signal)

    def get_by_category(self, category: ForecastCategory) -> list[Signal]:
        return [s for s in self.signals if s.category == category]

    def get_by_source(self, source: str) -> list[Signal]:
        return [s for s in self.signals if s.source == source]

    def get_since(self, since: datetime) -> list[Signal]:
        return [s for s in self.signals if s.timestamp >= since]

    def search(self, query: str) -> list[Signal]:
        q = query.lower()
        return [s for s in self.signals if q in s.content.lower() or q in s.source.lower()]


# ── Market Prior ───────────────────────────────────────────────────────────

class MarketPrior(BaseModel):
    """A prediction market prior for a forecast question."""
    prior_id: str = Field(default_factory=lambda: hashlib.md5(str(datetime.now(timezone.utc).timestamp()).encode()).hexdigest()[:12])
    forecast_id: str
    platform: str  # Kalshi, Polymarket, Metaculus, Manifold, etc.
    market_question: str
    market_url: str = ""
    probability: float = Field(ge=0.0, le=1.0)
    timestamp: datetime = Field(default_factory=lambda: datetime.now(timezone.utc))
    volume: float | None = None
    liquidity: float | None = None
    spread: float | None = None
    delta_1h: float | None = None
    delta_24h: float | None = None
    resolution_match_quality: ResolutionMatchQuality = ResolutionMatchQuality.CLOSE
    notes: str = ""


