# ── Causal Thesis Tree ─────────────────────────────────────────────────────

class CausalDriver(BaseModel):
    """A single driver in a causal thesis tree."""
    driver: str
    direction: Literal["increases", "decreases", "ambiguous"]
    weight: float = Field(ge=-1.0, le=1.0, default=0.0)
    evidence: list[str] = Field(default_factory=list)
    confidence: float = Field(ge=0.0, le=1.0, default=0.5)
    missing_information: list[str] = Field(default_factory=list)


class FrictionAnalysis(BaseModel):
    """Friction analysis for a forecast."""
    technical: list[str] = Field(default_factory=list)
    organizational: list[str] = Field(default_factory=list)
    economic: list[str] = Field(default_factory=list)
    regulatory: list[str] = Field(default_factory=list)
    social: list[str] = Field(default_factory=list)


class CausalThesisTree(BaseModel):
    """A causal thesis tree for structured reasoning about a forecast."""
    tree_id: str = Field(default_factory=lambda: hashlib.md5(str(datetime.now(timezone.utc).timestamp()).encode()).hexdigest()[:12])
    forecast_id: str
    root_question: str
    drivers: list[CausalDriver] = Field(default_factory=list)
    inversion_test: str = ""
    kill_conditions: list[str] = Field(default_factory=list)
    watch_indicators: list[str] = Field(default_factory=list)
    friction: FrictionAnalysis = Field(default_factory=FrictionAnalysis)


